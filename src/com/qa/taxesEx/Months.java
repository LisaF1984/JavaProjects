package com.qa.taxesEx;

public class Months {
	
	public static String monthsOfYear(String month) {
		
		switch (month) {
		
		case "january": 
		System.out.println("jan is cold");
		case "february":
			System.out.println("feb is snow");
		case "march":
			System.out.println("march has blossoms");
		case "april":
			System.out.println("april has chocolate");
		case "may":
			System.out.println("may is warmer");
		case "june":
			System.out.println("june is jas bday");
		case "july":
			System.out.println("july is summer");
		case "august":
			System.out.println("august is anniversary");
		case "september":
			System.out.println("sept im 37!");
		case "october":
			System.out.println("oct is holibobs");
		case "novemeber":
			System.out.println("nov is cfireworks");
		case "december":
			System.out.println("dec is santa");
			default:
				System.out.println("invalid year");
				break;	
		}
		return null;
		
	}
}

	
		


			
			

